// 1. Находим на странице ВСЕ нужные элементы как в игре "найди предмет"
let img = document.getElementById('slid') // Находим место для картинки (как фоторамку)
let nextBtn = document.getElementById('next') // Находим кнопку "Вперёд" (стрелка вправо)
let prevBtn = document.getElementById('prev') // Находим кнопку "Назад" (стрелка влево)

// 2. Счётчик - номер текущей картинки
// Начинаем с 0, потому что в компьютерах счёт начинается с нуля
let index = 0; // Представь, что у тебя 3 конфеты: они лежат под номерами 0, 1 и 2

// 3. Создаём "коробку" (массив) с нашими картинками
// Это как папка с фотографиями на компьютере
const images = [
    'images/1.jpg', // Картинка номер 0 (первая)
    'images/2.jpg', // Картинка номер 1 (вторая)  
    'images/3.jpg'  // Картинка номер 2 (третья)
]

// 4. Посмотрим в консоли (специальное окно для программистов), что у нас в "коробке"
console.log(images) // Это как заглянуть внутрь коробки и пересчитать фотографии

// 5. Говорим кнопке "Вперёд": "Слушай, когда по тебе кликнут мышкой!"
nextBtn.addEventListener('click', function () {
    // Когда кнопку нажали - начинается магия!

    // 6. Перелистываем на следующую картинку
    // index = index + 1 значит: "новый номер = старый номер + 1"
    // Было 0 → стало 1 (перешли с первой картинки на вторую)
    index = index + 1

    // 7. Проверяем, не дошли ли мы до конца
    // images.length = сколько всего картинок (у нас 3)
    // Если index == 3, значит мы посмотрели ВСЕ картинки и упёрлись в конец
    if (index == images.length) {
        // 8. Мы в конце альбома - возвращаемся к началу!
        // Как в карусели: после последней лошадки снова едет первая
        index = 0 // Возвращаемся к картинке номер 0 (самой первой)
    }

    // 9. Меняем картинку на экране
    // Говорим браузеру: "Покажи картинку с номером index из нашей коробки"
    img.src = images[index] // Например: img.src = 'images/2.jpg'

}) // Конец волшебства для кнопки "Вперёд"


// 10. Теперь тоже самое, но для кнопки "Назад"
prevBtn.addEventListener('click', function () {
    // Когда нажали "Назад"

    // 11. Листаем в обратную сторону
    // index = index - 1 значит: "новый номер = старый номер - 1"
    // Было 2 → стало 1 (с третьей картинки на вторую)
    index = index - 1

    // 12. Проверяем, не дошли ли мы до начала
    // Если index меньше 0, значит мы на первой картинке и хотим пойти ещё левее
    if (index < 0) {
        // 13. Перескакиваем в конец альбома!
        // images.length - 1 = номер последней картинки
        // Если картинок 3, то последняя будет под номером 2
        index = images.length - 1; // Переходим к самой последней картинке
    }

    // 14. Снова меняем картинку
    img.src = images[index]

})
